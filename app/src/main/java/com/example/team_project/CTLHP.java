package com.example.team_project;

public class CTLHP {
    private String tenMH;
    private int stc;

    public CTLHP(String tenMH, int stc) {
        this.tenMH = tenMH;
        this.stc = stc;
    }

    public String getTenMH() {
        return tenMH;
    }

    public void setTenMH(String tenMH) {
        this.tenMH = tenMH;
    }

    public int getStc() {
        return stc;
    }

    public void setStc(int stc) {
        this.stc = stc;
    }
}
